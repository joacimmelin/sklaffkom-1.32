#include "sklaff.h"

/*
 * down_string - converts string to lower case
 * args: string to convert (str)
 * ret:	pointer to string
 */

char *
down_string(char *str)
{
    char *tmp;

    tmp = str;
    while (*tmp) {
        if ((*tmp > 0x40) && (*tmp < 0x5e))
            *tmp += 0x20;
        tmp++;
    }
    return str;
}
